#include<iostream>
using namespace std;
int main(){
    int x = 5;
    cout<<x<<endl;  //yaha x = 5 hai
    x = 10;
    cout<<x<<endl;  //yaha x = 10 change ho gaya hai
    x += 10;
    cout<<x<<endl;  //yaha 10 + 10 = 20 ho jayega
    x *= 5;
    cout<<x<<endl;  //yaha 20 * 5 = 100 ho jayega
    x /= 10;
    cout<<x<<endl;  //yaha 100 / 10 = 10 ho jayega
}